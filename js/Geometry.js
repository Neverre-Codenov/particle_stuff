/**
 * Constructs a rectangle given:
 * 
 * param 1 top-left horizontal coordinate
 * param 2 top-left vertical cooridnate
 * param 3 width
 * param 4 height
 * 
 */

function Rectangle( top, left, width, height )
{

    this.top    = top;
    this.left   = left;
    this.width  = width;
    this.height = height;

    

}

